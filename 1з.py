день = int(input("Введите номер дня недели (1-7): "))

if день == 1:
    print("Понедельник")
elif день == 2:
    print("Вторник")
elif день == 3:
    print("Среда")
elif день == 4:
    print("Четверг")
elif день == 5:
    print("Пятница")
elif день == 6:
    print("Суббота")
elif день == 7:
    print("Воскресенье")
else:
    print("Неверный номер дня")
